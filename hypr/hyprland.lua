---- KEYBINDINGS ----
---------------------

local mainMod = "SUPER" 
local secondMod = "SUPER + SHIFT"

-- Launching Apps and Programs 
hl.bind(mainMod .. " + RETURN", hl.dsp.exec_cmd(terminal)) -- Terminal
hl.bind(mainMod .. " + SPACE", hl.dsp.exec_cmd(menu)) -- App Launcher
hl.bind(secondMod .. " + W", hl.dsp.exec_cmd("rofi-wifi-menu.sh")) -- Wifi Manager
hl.bind(secondMod .. " + B", hl.dsp.exec_cmd("rofi-bluetooth.sh")) -- Bluetooth Manager
hl.bind(secondMod .. " + D", hl.dsp.exec_cmd("hyprlock")) -- Lockscreen

-- Killing programs
hl.bind(mainMod .. " + Q", hl.dsp.window.close()) -- Current Window
hl.bind(mainMod .. " + M", hl.dsp.exec_cmd("command -v hyprshutdown >/dev/null 2>&1 && hyprshutdown || hyprctl dispatch 'hl.dsp.exit()'"))

-- Move focus with mainMod and vim keys
hl.bind(mainMod .. " + H",  hl.dsp.focus({ direction = "left" }))
hl.bind(mainMod .. " + L", hl.dsp.focus({ direction = "right" }))
hl.bind(mainMod .. " + K",    hl.dsp.focus({ direction = "up" }))
hl.bind(mainMod .. " + J",  hl.dsp.focus({ direction = "down" }))

-- Move focused window with secondMod and vim keys
hl.bind(secondMod .. " + H",  hl.dsp.window.move({ direction = "left" }))
hl.bind(secondMod .. " + L", hl.dsp.window.move({ direction = "right" }))
hl.bind(secondMod .. " + K",    hl.dsp.window.move({ direction = "up" }))
hl.bind(secondMod .. " + J",  hl.dsp.window.move({ direction = "down" }))

-- Resizing focused window with Alt and vim keys
hl.bind("ALT + H", hl.dsp.window.resize({ x = -50, y = 0, relative = true}))
hl.bind("ALT + L", hl.dsp.window.resize({ x = 50, y = 0, relative = true}))
hl.bind("ALT + K", hl.dsp.window.resize({ x = 0, y = 50, relative = true}))
hl.bind("ALT + J", hl.dsp.window.resize({ x = 0, y = -50, relative = true}))

-- Special window modes
hl.bind(secondMod .. " + T", hl.dsp.window.float({ action = "toggle" })) -- Disable window tyling 
hl.bind(secondMod .. " + F", hl.dsp.window.fullscreen({ mode = "maximized" })) -- Fullscreen with waybar
hl.bind(secondMod .. " + R", hl.dsp.window.fullscreen()) -- Fullscreen without waybar

-- Switch workspaces with mainMod + [0-9]
-- Move active window to a workspace with mainMod + SHIFT + [0-9]
for i = 1, 10 do
    local key = i % 10 -- 10 maps to key 0
    hl.bind(mainMod .. " + " .. key,       hl.dsp.focus({ workspace = i}))
    hl.bind(secondMod .. " + " .. key,     hl.dsp.window.move({ workspace = i }))
end

-- Move/resize windows with mainMod + LMB/RMB and dragging
hl.bind(mainMod .. " + mouse:272", hl.dsp.window.drag(),   { mouse = true })
hl.bind(mainMod .. " + mouse:273", hl.dsp.window.resize(), { mouse = true })

-- Laptop multimedia keys for volume, LCD brightness and audio operations
hl.bind("XF86AudioRaiseVolume", hl.dsp.exec_cmd("wpctl set-volume -l 1 @DEFAULT_AUDIO_SINK@ 5%+"), { locked = true, repeating = true })
hl.bind("XF86AudioLowerVolume", hl.dsp.exec_cmd("wpctl set-volume @DEFAULT_AUDIO_SINK@ 5%-"),      { locked = true, repeating = true })
hl.bind("XF86AudioMute",        hl.dsp.exec_cmd("wpctl set-mute @DEFAULT_AUDIO_SINK@ toggle"),     { locked = true, repeating = true })
hl.bind("XF86AudioMicMute",     hl.dsp.exec_cmd("wpctl set-mute @DEFAULT_AUDIO_SOURCE@ toggle"),   { locked = true, repeating = true })
hl.bind("XF86MonBrightnessUp",  hl.dsp.exec_cmd("brightnessctl -e4 -n2 set 5%+"),                  { locked = true, repeating = true })
hl.bind("XF86MonBrightnessDown",hl.dsp.exec_cmd("brightnessctl -e4 -n2 set 5%-"),                  { locked = true, repeating = true })
hl.bind("XF86AudioNext",  hl.dsp.exec_cmd("playerctl next"),       { locked = true })
hl.bind("XF86AudioPause", hl.dsp.exec_cmd("playerctl play-pause"), { locked = true })
hl.bind("XF86AudioPlay",  hl.dsp.exec_cmd("playerctl play-pause"), { locked = true })
hl.bind("XF86AudioPrev",  hl.dsp.exec_cmd("playerctl previous"),   { locked = true })


